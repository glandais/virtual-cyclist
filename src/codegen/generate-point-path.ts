#!/usr/bin/env node
/**
 * Code generator for Point.ts and Path.ts accessor methods.
 *
 * This script generates TypeScript code from field definitions.
 * Run: npm run generate:point
 */

import { writeFileSync } from 'fs';
import { resolve } from 'path';
import { FIELD_DEFINITIONS, TOTAL_FIELD_COUNT } from './field-definitions';

const PROJECT_ROOT = resolve(__dirname, '..');

/**
 * Convert field property name to PascalCase for method names.
 * Examples: lat -> Latitude, pCyclistRaw -> PCyclistRaw
 */
function toPascalCase(prop: string): string {
    // Handle special cases like pCyclistRaw -> PCyclistRaw
    if (prop.startsWith('p') && prop.charAt(1) === prop.charAt(1).toUpperCase()) {
        return prop.charAt(0).toUpperCase() + prop.slice(1);
    }
    return prop.charAt(0).toUpperCase() + prop.slice(1);
}

/**
 * Generate Point.ts file completely.
 */
function generatePointFile() {
    console.log('🔨 Generating Point.ts...');

    const lines: string[] = [];

    // Header
    lines.push('// ⚠️ THIS FILE IS AUTO-GENERATED - DO NOT EDIT MANUALLY');
    lines.push('// Generated by: scripts/generate-point-path.ts');
    lines.push('// Source: src/codegen/field-definitions.ts');
    lines.push('// To modify: Update field-definitions.ts and run: npm run generate:point');
    lines.push('');

    // PointField enum
    lines.push('/**');
    lines.push(' * Enum defining the index of each field in the chunked array storage.');
    lines.push(' * Based on PropertyKeys.java from gpx2web project.');
    lines.push(
        ` * Each point has exactly ${TOTAL_FIELD_COUNT} numeric values stored contiguously.`
    );
    lines.push(' */');
    lines.push('export enum PointField {');

    let i = 0;
    for (const category of FIELD_DEFINITIONS) {
        lines.push(`    // ${category.name} (${category.fields.length} properties)`);

        for (const field of category.fields) {
            lines.push(`    ${field.name} = ${i++}, // ${field.comment}`);
        }
        lines.push('');
    }

    lines.push('}');
    lines.push('');
    lines.push('export enum PointFieldName {');

    for (const category of FIELD_DEFINITIONS) {
        lines.push(`    // ${category.name} (${category.fields.length} properties)`);

        for (const field of category.fields) {
            lines.push(`    ${field.prop} = '${field.prop}', // ${field.comment}`);
        }
        lines.push('');
    }

    lines.push('}');
    lines.push('');

    lines.push('export const fieldToPointField: Record<PointFieldName, PointField> = {');

    for (const category of FIELD_DEFINITIONS) {
        lines.push(`    // ${category.name} (${category.fields.length} properties)`);

        for (const field of category.fields) {
            lines.push(`    ${field.prop}: PointField.${field.name}, // ${field.comment}`);
        }
        lines.push('');
    }

    lines.push('}');
    lines.push('');

    // FIELDS_PER_POINT constant
    lines.push('/**');
    lines.push(' * Total number of fields per point in the chunked storage.');
    lines.push(' */');
    lines.push(`export const FIELDS_PER_POINT = ${i};`);
    lines.push('');

    // Point interface
    lines.push('/**');
    lines.push(
        ` * Interface representing a complete point with all ${TOTAL_FIELD_COUNT} properties.`
    );
    lines.push(' * All values are stored as numbers with appropriate unit conversions.');
    lines.push(' *');
    lines.push(' * Note: Angles are stored in RADIANS for efficient physics calculations.');
    lines.push(' */');
    lines.push('export interface Point {');

    for (const category of FIELD_DEFINITIONS) {
        lines.push(`    // ${category.name}`);

        for (const field of category.fields) {
            lines.push(`    readonly ${field.prop}: number; // ${field.comment}`);
        }
        lines.push('');
    }

    lines.push('}');
    lines.push('');

    // PointWritable type
    lines.push('type Writable<T> = {');
    lines.push('    -readonly [K in keyof T]: T[K];');
    lines.push('};');
    lines.push('');
    lines.push('export type PointWritable = Writable<Point>;');
    lines.push('');

    // EMPTY_POINT constant
    lines.push('export const EMPTY_POINT: Point = {');

    for (const category of FIELD_DEFINITIONS) {
        lines.push(`    // ${category.name}`);

        for (const field of category.fields) {
            lines.push(`    ${field.prop}: NaN,`);
        }
    }

    lines.push('};');

    // Write to file
    const content = lines.join('\n') + '\n';
    const filePath = resolve(PROJECT_ROOT, 'types/path/Point.ts');
    writeFileSync(filePath, content, 'utf-8');

    console.log(`✅ Generated Point.ts`);
}

