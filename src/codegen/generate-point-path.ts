/**
 * Generate accessor methods for Path.ts
 * Returns the generated method code as a string
 */
function generatePathAccessors(lines: string[]) {
    console.log('🔨 Generating Path.ts accessor methods...');

    // Group fields by category for better organization
    for (const category of FIELD_DEFINITIONS) {
        lines.push(`    // === ${category.name} Accessors ===`);
        lines.push('');

        for (const field of category.fields) {
            const pascalName = field.methodName || toPascalCase(field.prop);

            // Standard getter
            lines.push(`    get${pascalName}(pointIndex: number): number {`);
            lines.push(`        return super.getField(pointIndex, PointField.${field.name});`);
            lines.push(`    }`);
            lines.push('');

            // Degree variant getter
            if (field.getDegrees) {
                lines.push(`    get${pascalName}Deg(pointIndex: number): number {`);
                lines.push(
                    `        return toDegrees(super.getField(pointIndex, PointField.${field.name}));`
                );
                lines.push(`    }`);
                lines.push('');
            }

            // Standard setter
            const setterParam = field.setSpecial === 'date' ? 'number | Date' : 'number';
            lines.push(`    set${pascalName}(pointIndex: number, value: ${setterParam}): void {`);

            if (field.setSpecial === 'date') {
                lines.push(
                    `        const timestamp = value instanceof Date ? value.getTime() : value;`
                );
                lines.push(
                    `        super.setField(pointIndex, PointField.${field.name}, timestamp);`
                );
            } else {
                lines.push(`        super.setField(pointIndex, PointField.${field.name}, value);`);
            }

            lines.push(`    }`);
            lines.push('');

            // Special getter variant
            if (field.getSpecial === 'date') {
                lines.push(`    get${pascalName}AsDate(pointIndex: number): Date {`);
                lines.push(`        return new Date(this.get${pascalName}(pointIndex));`);
                lines.push(`    }`);
                lines.push('');
            }
        }
    }
}

/**
 * Generate addPoint method implementation
 */

function generateAddPointMethod(lines: string[]) {
    console.log('🔨 Generating addPoint method...');

    lines.push('    /**');
    lines.push('     * Adds a new point with the provided data.');
    lines.push(`     * @param data Complete point data with all ${TOTAL_FIELD_COUNT} properties`);
    lines.push('     * @returns The index of the newly added point');
    lines.push('     */');
    lines.push('    addPoint(data: Point): number {');
    lines.push('        const pointIndex = this.pointCount;');
    lines.push('        this.ensureCapacity(pointIndex + 1);');
    lines.push('');
    lines.push('        // Increment point count first so bounds checking works');
    lines.push('        this.pointCount++;');
    lines.push('');
    lines.push('        // Set all fields from the provided data');

    for (const category of FIELD_DEFINITIONS) {
        lines.push(`        // ${category.name}`);

        for (const field of category.fields) {
            const pascalName = field.methodName || toPascalCase(field.prop);
            lines.push(`        this.set${pascalName}(pointIndex, data.${field.prop});`);
        }
        lines.push('');
    }

    lines.push('        return pointIndex;');
    lines.push('    }');
}

/**
 * Generate getPointData method implementation
 */
function generateGetPointDataMethod(lines: string[]) {
    console.log('🔨 Generating getPointData method...');

    lines.push('    /**');
    lines.push('     * Gets all data for a specific point.');
    lines.push('     */');
    lines.push('    getPointData(pointIndex: number): Point {');
    lines.push('        return {');

    for (const category of FIELD_DEFINITIONS) {
        lines.push(`            // ${category.name}`);

        for (const field of category.fields) {
            const pascalName = field.methodName || toPascalCase(field.prop);
            lines.push(`            ${field.prop}: this.get${pascalName}(pointIndex),`);
        }
    }

    lines.push('        };');
    lines.push('    }');
}

/**
 * Generate interpolatePoint method implementation
 */
function generateInterpolatePointMethod(lines: string[]) {
    console.log('🔨 Generating interpolatePoint method...');

    lines.push('        // Helper function for strict NaN handling');
    lines.push(
        '        protected interpolateValue(v1: number, v2: number, coef: number): number {'
    );
    lines.push('            if (isNaN(v1) || isNaN(v2)) {');
    lines.push('                return NaN;');
    lines.push('            }');
    lines.push('            return v1 + (v2 - v1) * coef;');
    lines.push('        };');
    lines.push('');

    lines.push('    /**');
    lines.push('     * Interpolates a new point between two existing points.');
    lines.push('     *');
    lines.push('     * Uses linear interpolation for all numeric fields:');
    lines.push('     * - result = p1 + (p2 - p1) × coef');
    lines.push('     *');
    lines.push('     * **NaN Handling (STRICT):**');
    lines.push('     * - If either p1 or p2 value is NaN, result is NaN');
    lines.push('     * - This ensures data integrity for incomplete GPS data');
    lines.push('     *');
    lines.push('     * @param index1 Index of first point');
    lines.push('     * @param index2 Index of second point');
    lines.push('     * @param coef Interpolation coefficient (0 = p1, 1 = p2, 0.5 = midpoint)');
    lines.push('     * @returns Interpolated point');
    lines.push('     */');
    lines.push(
        '    public interpolatePoint(index1: number, index2: number, coef: number): Point {'
    );
    lines.push('        const p1 = this.getPointData(index1);');
    lines.push('        const p2 = this.getPointData(index2);');
    lines.push('');
    lines.push('        return {');

    for (const category of FIELD_DEFINITIONS) {
        lines.push(`            // ${category.name}`);

        for (const field of category.fields) {
            lines.push(
                `            ${field.prop}: this.interpolateValue(p1.${field.prop}, p2.${field.prop}, coef),`
            );
        }
    }

    lines.push('        };');
    lines.push('    }');
}

/**
 * Main generation function
 */
function main() {
    console.log('🚀 Starting code generation...\n');

    // Generate Point.ts
    generatePointFile();
    generateAbstractPathFile();

    console.log('\n✅ Code generation complete!');
    console.log('\n⚠️  Note: Path.ts methods printed above for review.');
    console.log('   These can be manually copied into Path.ts or automated in future versions.');
}

// Run the generator
main();
